/**
 * <p> Metodo che setta i dettagli del file selezionati per la modifica nei campi del dialogbox .</p>
 */
    private void setFields (){
       titTF.setText(lib.getElementAt(index).getTitolo());
      autTF.setText(lib.getElementAt(index).getAutore());
      annTF.setText(lib.getElementAt(index).getAnno());
      numpagTF.setText(lib.getElementAt(index).getNumPagine());
      perTF.setText(lib.getElementAt(index).getPercorso());
             
    }
    /**
     * <p> Metodo che risalva le modifiche effettuate. </p>
     */
    private void resave(){
         if(titTF.getText().equals("")  && autTF.getText().equals("") ){
             JOptionPane.showMessageDialog(this," Titolo Autore e Anno non possono essere vuoti");
             this.dispose();
         }
         else{
             lib.setTitle(index, titTF.getText());
             lib.setAuthor(index, autTF.getText());
             lib.setPagenumb(index,numpagTF.getText());
             lib.setYear(index,annTF.getText());
             
             }
            
           
         
        
    }
    
    
